public class Cliente {
	public String nome = "0";
	public String indirizzo = "0";
	public String numero = 0;
	public double saldo = 0;
	
	public Cliente() {
		this.nome="0";
		this.indirizzo="0";
		this.numero="0";
		this.saldo=0;
	}
	public Cliente(String nome1, String indirizzo1, String numero1, double saldo1) {
		this.nome=nome1;
		this.indirizzo=indirizzo1;
		this.numero=numero1;
		this.saldo=saldo1;
	}
}
